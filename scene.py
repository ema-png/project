import turtle
t = turtle.Turtle()
t.speed(0) # 1:slowest, 3:slow, 5:normal, 10:fast, 0:fastest
def tree(h):
  t.fillcolor("green")
              
  t.forward(h)
  for i in range(3):
    t.begin_fill()
    t.left(150)
    t.forward(h/4)
    t.left(120)
    t.forward(h/4)
    t.left(120)
    t.forward(h/4)
    t.left(150)
    t.forward(0.2165*h)
    t.left(180)
    t.end_fill()
  t.left(180)
  t.forward(h-3*(0.2165*h))
  
